#!/bin/bash

# vim commands

Esc # normal mode
i # insert mode
v # visual mode
: # command mode

# from command mode:
:w # write
:q # quit
:q! # quit and discard changes
:wq # write and quit

# from normal mode:
yy # copy entire line
p # paste
dd # delete entire line

# copy to system clipboard
"+y    " # don't include last " 
# paste from system clipboard
"+p    " # don't include last "

